# ----------------------------------------------------------------------------
# 1. Αφαίρεση τόνων (για ταύτιση λέξεων ανεξαρτήτως τονισμού)
# ----------------------------------------------------------------------------
def strip_accents(text: str) -> str:
    nfkd = unicodedata.normalize("NFD", text)
    return "".join(c for c in nfkd if unicodedata.category(c) != "Mn")


