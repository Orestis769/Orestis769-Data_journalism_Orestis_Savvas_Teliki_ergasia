_NEGATIONS = set(strip_accents(w) for w in "δεν μην μη οχι ουτε χωρις".split())


def sentiment_scores(text: str):
    """
    Επιστρέφει dict με: pos, neg (πλήθος), polarity ∈ [-1,1],
    label (positive/negative/neutral) και κυρίαρχο συναίσθημα.
    Απλός χειρισμός άρνησης: αν προηγείται αρνητικό μόριο, αντιστρέφεται.
    """
    toks = [strip_accents(t) for t in tokenize(text)]
    pos = neg = 0
    for i, t in enumerate(toks):
        negated = i > 0 and toks[i - 1] in _NEGATIONS
        if t in POSITIVE_WORDS:
            neg += 1 if negated else 0
            pos += 0 if negated else 1
        elif t in NEGATIVE_WORDS:
            pos += 1 if negated else 0
            neg += 0 if negated else 1
    total = pos + neg
    polarity = (pos - neg) / total if total else 0.0
    if polarity > 0.05:
        label = "positive"
    elif polarity < -0.05:
        label = "negative"
    else:
        label = "neutral"
    emo_counts = {}
    for emo, lex in EMOTION_LEXICON.items():
        emo_counts[emo] = sum(1 for t in toks if t in lex)
    dominant = max(emo_counts, key=emo_counts.get) if any(emo_counts.values()) else "ουδέτερο"
    return {
        "pos_words": pos,
        "neg_words": neg,
        "polarity": round(polarity, 4),
        "sentiment": label,
        "emotion": dominant,
        **{f"emo_{k}": v for k, v in emo_counts.items()},
    }
