# ----------------------------------------------------------------------------
# 3. Tokenizer (κρατά μόνο ελληνικά/λατινικά γράμματα)
# ----------------------------------------------------------------------------
_token_re = re.compile(r"[Α-Ωα-ωΆ-Ώά-ώA-Za-z]+")

def tokenize(text: str):
    return _token_re.findall(text.lower())


# ----------------------------------------------------------------------------
# 4. Απλός ελληνικός stemmer (αφαίρεση κοινών καταλήξεων)
#    Προσέγγιση του lemmatization χωρίς εξωτερική βιβλιοθήκη.
# ----------------------------------------------------------------------------
_SUFFIXES = sorted([
    "ιοτητας","ιοτητα","οτητας","οτητα","ιζουμε","ισουμε","ονται","ομαστε",
    "εστε","ουμε","ετε","ουν","εις","ειτε","ησαν","ησε","ησα","ωντας","οντας",
    "ματος","ματα","ματων","εων","εως","ιων","ιου","ιοι","ιες","ιας",
    "ους","ος","ου","οι","ων","ες","ας","ης","ην","αν","ον","εν","ις","υς",
    "α","ε","η","ι","ο","υ","ω","ν","ς",
], key=len, reverse=True)

def stem(word: str) -> str:
    w = strip_accents(word.lower())
    if len(w) <= 4:
        return w
    for suf in _SUFFIXES:
        if w.endswith(suf) and len(w) - len(suf) >= 3:
            return w[: -len(suf)]
    return w


def preprocess(text: str, do_stem: bool = True):
    """Tokenize -> strip accents -> remove stopwords -> (stem)."""
    out = []
    for tok in tokenize(text):
        t = strip_accents(tok)
        if len(t) < 3 or t in GREEK_STOPWORDS:
            continue
        out.append(stem(t) if do_stem else t)
    return out


